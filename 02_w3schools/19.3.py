# Max of Two
# Write a function that receives two numbers and returns the larger one.

def my_function(a, b):
    if a > b:
        print(a)
    elif a == b:
        print("Так вони ж рівнозначні блеать")
    else:
        print(b)
my_function(1, 1)
my_function(1, 3)
my_function(4, 2)